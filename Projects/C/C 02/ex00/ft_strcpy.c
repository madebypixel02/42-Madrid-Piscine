#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	char *dest0;

	dest0 = dest;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	if (*src == '\0')
		*dest = '\0';
	return (dest0);
}
