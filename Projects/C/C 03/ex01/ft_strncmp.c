int ft_strcmp(char *s1, char *s2, unsigned int n)
{
	int i;

	i = 0;
	while(i != n - 1)
	{
		if (*s1 < *s2)
			return (*s2 - *s1);
		else if (*s2 < *s1)
			return (*s1 - *s2);
		s1++;
		s2++;
		i++;
	}
	return (0);
}