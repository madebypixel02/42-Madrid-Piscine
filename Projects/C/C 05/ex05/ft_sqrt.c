int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb * nb <= 2147483647)
	{
		while (i <= nb / 2 || nb == 1)
		{
			if (i * i == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
