#include <unistd.h>

void	ft_print_comb(void)
{
	char c;
	char d;
	char u;

	c = '/';
	while (c++ < '9')
	{
		d = '/';
		while (d++ < '9')
		{
			u = '/';
			while (u++ < '9')
			{
				if (c < d && d < u)
				{
					write(1, &c, 1);
					write(1, &d, 1);
					write(1, &u, 1);
					if (c != '7' || d != '8' || u != '9')
						write(1, ", ", 2);
				}
			}
		}
	}
}
