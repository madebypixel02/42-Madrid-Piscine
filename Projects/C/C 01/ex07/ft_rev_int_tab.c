void	ft_rev_int_tab(int *tab, int size)
{
	int temp;
	int *tab2;
	int i;

	i = 0;
	tab2 = tab + size - 1;
	while (i < size / 2)
	{
		temp = *tab;
		*tab = *tab2;
		*tab2 = temp;
		tab++;
		tab2--;
		i++;
	}
}
